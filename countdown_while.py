'''
PYTHON LAB: countdown_while.py

Write a while-loop and inside of your while-loop count down backward from 10 to 0.
Print starting to countdwn backward from 10-0
Print countdown is ove! At the end
'''
# Initialize the counter
counter = 10
print("starting to countdown backward from 10-0")
#while  loop part
while counter >= 0:
    print(counter)
    counter -= 1
print("Countdown is over!")    

